import fs from "fs";

const sourcePath = process.argv[2] || "/tmp/bibble-kjv.json";
const sourceUrl = "https://raw.githubusercontent.com/nick1udwig/bible/master/json/en_kjv.json";

function readJson(path) {
  const text = fs.readFileSync(path, "utf8").replace(/^\uFEFF/, "");
  return JSON.parse(text);
}

function cString(value) {
  return JSON.stringify(String(value));
}

function wrapNumbers(numbers, perLine) {
  const lines = [];
  for (let index = 0; index < numbers.length; index += perLine) {
    lines.push("  " + numbers.slice(index, index + perLine).join(", "));
  }
  return lines.join(",\n");
}

const books = readJson(sourcePath);

if (!Array.isArray(books) || books.length !== 66) {
  throw new Error(`Expected 66 books, got ${Array.isArray(books) ? books.length : typeof books}`);
}

const bookMeta = books.map((book) => ({
  abbrev: book.abbrev,
  name: book.name,
  verseCounts: book.chapters.map((chapter) => chapter.length)
}));
const chapterCounts = [];
const chapterOffsets = [];
const verseCounts = [];
let chapterOffset = 0;

books.forEach((book) => {
  if (!book || typeof book.name !== "string" || !Array.isArray(book.chapters)) {
    throw new Error("Invalid KJV book shape");
  }
  chapterOffsets.push(chapterOffset);
  chapterCounts.push(book.chapters.length);
  chapterOffset += book.chapters.length;
  book.chapters.forEach((chapter) => {
    if (!Array.isArray(chapter)) {
      throw new Error(`Invalid chapter in ${book.name}`);
    }
    verseCounts.push(chapter.length);
  });
});

fs.writeFileSync(
  "src/pkjs/bible-meta.js",
  [
    "/* Generated by scripts/generate-kjv-data.mjs.",
    ` * Source: ${sourceUrl}`,
    " * Contains book/chapter/verse metadata only; KJV text downloads into PKJS state at runtime.",
    " */",
    `"use strict";`,
    "",
    `module.exports = ${JSON.stringify(bookMeta)};`,
    ""
  ].join("\n")
);

fs.writeFileSync(
  "src/c/bible_meta.h",
  [
    "/* Generated by scripts/generate-kjv-data.mjs. */",
    "#ifndef BIBBLE_BIBLE_META_H",
    "#define BIBBLE_BIBLE_META_H",
    "",
    "#include <stdint.h>",
    "",
    `#define BIBBLE_BOOK_COUNT ${books.length}`,
    `#define BIBBLE_CHAPTER_COUNT ${verseCounts.length}`,
    "",
    "static const char *const BIBBLE_BOOK_NAMES[BIBBLE_BOOK_COUNT] = {",
    books.map((book) => `  ${cString(book.name)}`).join(",\n"),
    "};",
    "",
    "static const uint8_t BIBBLE_BOOK_CHAPTER_COUNTS[BIBBLE_BOOK_COUNT] = {",
    wrapNumbers(chapterCounts, 16),
    "};",
    "",
    "static const uint16_t BIBBLE_BOOK_CHAPTER_OFFSETS[BIBBLE_BOOK_COUNT] = {",
    wrapNumbers(chapterOffsets, 12),
    "};",
    "",
    "static const uint8_t BIBBLE_CHAPTER_VERSE_COUNTS[BIBBLE_CHAPTER_COUNT] = {",
    wrapNumbers(verseCounts, 24),
    "};",
    "",
    "#endif",
    ""
  ].join("\n")
);

console.log(`Generated ${books.length} books, ${verseCounts.length} chapters, ${verseCounts.reduce((sum, value) => sum + value, 0)} verses.`);
